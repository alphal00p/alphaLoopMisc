import pprint
from pprint import pformat

class FormatPrinter(pprint.PrettyPrinter):

    def __init__(self, **kwargs): 
        kwargsNext = { key: kwargs[key] for key in kwargs if key != 'formats' }
        super().__init__(**kwargsNext) 
        self.formats = kwargs['formats']

    def format(self, obj, ctx, maxlvl, lvl):
        if type(obj) is tuple:
            res = "( {:+.16e}, {:+.16e}, {:+.16e} )".format(obj[0],obj[1],obj[2])
            return res, 1, 0
        if type(obj) is complex:
            res = "{:+.16e}".format(obj)
            return res, 1, 0
        elif type(obj) in self.formats:
            return self.formats[type(obj)] % obj, 1, 0
        else:
            return pprint.PrettyPrinter.format(self, obj, ctx, maxlvl, lvl)

